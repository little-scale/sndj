def tile_2bpp(rows8):
    """rows8: 8 strings of 8 chars ('#'=colour 3). Returns 16 bytes 2bpp."""
    out = bytearray()
    for r in rows8:
        b = 0
        for i, c in enumerate(r):
            if c == '#':
                b |= 0x80 >> i
        out += bytes((b, b))  # colour 3: bit set in both planes
    return bytes(out)


def glyph_rows(ch):
    rows = G[ch]
    return ['.' + r + '..' for r in rows] + ['........']


def main(out_path):
    data = bytearray()
    for code in range(32, 96):
        data += tile_2bpp(glyph_rows(chr(code)))
    for _name, rows in EXTRA:
        data += tile_2bpp(rows)
    with open(out_path, 'wb') as f:
        f.write(data)
    print(f"makefont: {len(G)} ascii + {len(EXTRA)} extra glyphs -> {out_path} ({len(data)} bytes)")


if __name__ == '__main__':
    main(sys.argv[1] if len(sys.argv) > 1 else 'build/font.bin')
